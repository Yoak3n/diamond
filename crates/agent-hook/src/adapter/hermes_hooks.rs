mod tests {
    use super::*;

    #[test]
    fn test_gateway_handler_generates_valid_python() {
        let handler = generate_gateway_handler("ws://127.0.0.1:9210/hook");
        assert!(handler.contains("HUB_WS_URL = \"ws://127.0.0.1:9210/hook\""));
        assert!(handler.contains("HUB_HTTP_URL = \"http://127.0.0.1:9210\""));
        assert!(handler.contains("async def handle(event_type, context):"));
    }

    #[test]
    fn test_plugin_init_generates_valid_python() {
        let init = generate_plugin_init("ws://127.0.0.1:9210/hook");
        assert!(init.contains("HUB_HTTP_URL = \"http://127.0.0.1:9210\""));
        assert!(init.contains("def register(ctx):"));
        assert!(init.contains("transform_llm_output"));
        assert!(init.contains("assistant:response"));
    }

    #[test]
    fn test_plugin_init_has_no_triple_quotes() {
        let init = generate_plugin_init("ws://127.0.0.1:9210/hook");
        assert!(!init.contains("\"\"\""));
    }
}
